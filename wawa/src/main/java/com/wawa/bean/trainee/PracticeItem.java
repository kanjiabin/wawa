package com.wawa.bean.trainee;

public class PracticeItem {

	String examsType;
	String examsName;
	String traineeId;
	String practiceDate;
	String status;
	String practiceScore;
	public String getExamsType() {
		return examsType;
	}
	public void setExamsType(String examsType) {
		this.examsType = examsType;
	}
	public String getExamsName() {
		return examsName;
	}
	public void setExamsName(String examsName) {
		this.examsName = examsName;
	}
	public String getTraineeId() {
		return traineeId;
	}
	public void setTraineeId(String traineeId) {
		this.traineeId = traineeId;
	}
	public String getPracticeDate() {
		return practiceDate;
	}
	public void setPracticeDate(String practiceDate) {
		this.practiceDate = practiceDate;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPracticeScore() {
		return practiceScore;
	}
	public void setPracticeScore(String practiceScore) {
		this.practiceScore = practiceScore;
	}

}
